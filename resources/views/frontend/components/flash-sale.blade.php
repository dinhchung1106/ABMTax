<script>
document.addEventListener('DOMContentLoaded', function() {
    // Countdown Timer
    function updateCountdown() {
        const countdowns = document.querySelectorAll('.flash-sale-section .countdown');
        
        countdowns.forEach(countdown => {
            const endDate = new Date(countdown.dataset.countdown).getTime();
            const now = new Date().getTime();
            const distance = endDate - now;

            if (distance < 0) {
                countdown.innerHTML = '<div class="expired">Đã kết thúc</div>';
                return;
            }

            const days = Math.floor(distance / (1000 * 60 * 60 * 24));
            const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
            const seconds = Math.floor((distance % (1000 * 60)) / 1000);

            const daysElement = countdown.querySelector('.days');
            const hoursElement = countdown.querySelector('.hours');
            const minutesElement = countdown.querySelector('.minutes');
            const secondsElement = countdown.querySelector('.seconds');

            if (daysElement) daysElement.textContent = String(days).padStart(2, '0');
            if (hoursElement) hoursElement.textContent = String(hours).padStart(2, '0');
            if (minutesElement) minutesElement.textContent = String(minutes).padStart(2, '0');
            if (secondsElement) secondsElement.textContent = String(seconds).padStart(2, '0');
        });
    }

    // Update countdown every second
    setInterval(updateCountdown, 1000);
    updateCountdown();

    // Initialize WOW.js for animations
    if (typeof WOW !== 'undefined') {
        new WOW().init();
    }
});
</script> 